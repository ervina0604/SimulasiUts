    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int selectedRow = table.getSelectedRow();
        if (selectedRow != -1) {
            String isbn = table.getValueAt(selectedRow, 0).toString();
            String judul_buku = table.getValueAt(selectedRow, 1).toString();
            int tahun_terbit = Integer.parseInt(table.getValueAt(selectedRow, 2).toString());
            String penerbit = table.getValueAt(selectedRow, 3).toString();

            FieldIsbn.setText(isbn);
            FieldJdlBuku.setText(judul_buku);
            FieldThnTerbit.setText(String.valueOf(tahun_terbit));
            FieldPenerbit.setText(penerbit);
        }
    }//GEN-LAST:event_tableMouseClicked

